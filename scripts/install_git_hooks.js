const { execSync } = require("child_process");
const path = require("path");

const REPO_ROOT = path.resolve(__dirname, "..");

function main() {
  execSync("git config core.hooksPath .githooks", {
    cwd: REPO_ROOT,
    stdio: "inherit",
  });
  process.stdout.write("[hooks] core.hooksPath set to .githooks\n");
}

main();
